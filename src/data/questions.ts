// Algoritmo mejorado de Fisher-Yates para selección aleatoria
export const getRandomQuestions = (count: number = 10): Question[] => {
  const questionsCopy = [...footballQuestions];
  
  // Fisher-Yates shuffle algorithm - más eficiente y verdaderamente aleatorio
  for (let i = questionsCopy.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [questionsCopy[i], questionsCopy[j]] = [questionsCopy[j], questionsCopy[i]];
  }
  
  return questionsCopy.slice(0, Math.min(count, questionsCopy.length));
};

// Algoritmo mejorado para quiz diario con semilla más robusta
export const getDailyQuestions = (date: string): Question[] => {
  const questionsCopy = [...footballQuestions];
  
  // Crear semilla más robusta basada en la fecha
  let seed = 0;
  for (let i = 0; i < date.length; i++) {
    seed = ((seed << 5) - seed + date.charCodeAt(i)) & 0xffffffff;
  }
  
  // Generador pseudoaleatorio con semilla
  const seededRandom = (s: number) => {
    s = Math.sin(s) * 10000;
    return s - Math.floor(s);
  };
  
  // Shuffle con semilla
  for (let i = questionsCopy.length - 1; i > 0; i--) {
    const j = Math.floor(seededRandom(seed + i) * (i + 1));
    [questionsCopy[i], questionsCopy[j]] = [questionsCopy[j], questionsCopy[i]];
  }
  
  return questionsCopy.slice(0, 5); // 5 preguntas para el quiz diario
};

